package fundamentos.operadores;

public class Atribuicao {
    public static void main(String[] args) {
        int a = 3;
        int b = a;
        int c = a+b;

        //modos de atribuição.

        c+=b; // c= c+b;
        c-=a; //c =c -a;
        c*= b; // c= c*b;
        c/=a; // c = c/a;

        c++; //modo de atribuição.


    }
}
